

function CheckPolyDefinesTRdegdExtnatp(f, d,p)
    if not IsIrreducible(f) or Degree(f) ne d then
        return false;
    else
        //first check Eisenstein:
        coefs := Coefficients(f);
        if coefs[1] mod p ne 0 and Valuation(coefs[d+1], p) eq 1  and Minimum([Valuation(coefs[i], p) : i in [2..d]]) ge 1 then
            return true;
        elif coefs[d+1] mod p ne 0 and Valuation(coefs[1], p) eq 1  and Minimum([Valuation(coefs[i], p) : i in [2..d]]) ge 1 then
            return true;
        end if;

    // print "Polynomial not Eisenstein -- verification may take longer";
    // print f;

        K := NumberField(f);
        OK := RingOfIntegers(K);
        factors := Factorization(ideal<OK | p>);
        if #factors eq 1 and factors[1][2] eq d then
            return true;
        else
            return false;
        end if;
    end if;
end function;

function VerifyTotallyRamifiedDegree3Point(Cdesc, T, p)
    polys := Cdesc[3];
    cond := Cdesc[2];
    h := polys[2];
    g := polys[1];
    factors := Factorization(Polynomial(h)^2 + 4*Polynomial(g));
    assert #factors eq 2;

    h0 := factors[1][1];
    h2 := Parent(h0)!(-(Polynomial(h)^2 + 4*Polynomial(g))/h0);
    assert h0*(-h2) eq Polynomial(h)^2 + 4*Polynomial(g);

    new_poly := h0*T^2 + h2;

    return CheckPolyDefinesTRdegdExtnatp(new_poly, 3,p), new_poly;

end function;


function VerifyTotallyRamifiedDegree5Point(Cdesc, asbs, p)
    
    as := [asbs[i] : i in [1,2]];
    bs := [asbs[i] : i in [3,4]];
    
    polys := Cdesc[3];
    cond := Cdesc[2];
    h := polys[2];
    g := polys[1];
    factors := Factorization(Polynomial(h)^2 + 4*Polynomial(g));
    assert #factors eq 2;

    h0 := factors[1][1];
    h2 := Parent(h0)!(-(Polynomial(h)^2 + 4*Polynomial(g))/h0);
    assert h0*(-h2) eq Polynomial(h)^2 + 4*Polynomial(g);

    a := Polynomial(as);
    b := Polynomial(bs);

    f := h0*b^2 + h2*a^2;
    return CheckPolyDefinesTRdegdExtnatp(f, 5,p), f;

end function;




